import Cocoa

//Type Annotations
//Instead of letting the system infer the appropriate data type:
let surname = "Lasso"
//we can explicitly declare it
var score: Double = 0 //normally would've been an Int
//Useful for creating empty arrays
var teams: [String] = [String]()
//Also can be done either way:
var cities: [String] = []
var clues = [String]()

//You can define empty variables (HELL yes)
let username: String
username = "@twostraws"
print(username)
